					müssen sich die inzwischen über 300 Nachwuchsmusiker:innen nicht festlegen, denn bei
					Musica Altona wird in verschiedenen Gruppen »Crossover« musiziert:
					von Schlagzeug, Blasinstrumenten, Klavier, Keyboard, afrikanischen Trommeln über Saz,
					Ukulele und Gitarre bis zur klassischen Geige, Cello sowie einem Shanty Chor und
					Band Projekten.
				</p>
